pub struct BaseNodeListDeserializer<'a> {
    input: &'a str,
}

#[derive(Debug, Clone, Error)]
pub enum Error {
    #[error("attempted to deserialize a primitive from a node list")]
    AtNodeList,

    #[error("error")]
    Custom(String),
}

impl de::Error for Error {
    fn custom<T>(msg: T) -> Self
    where
        T: Display,
    {
        Error::Custom(msg.to_string())
    }
}

